package Calculadora;

public class Metodos {

    public static int sum(int algarismoA, int algarismoB) { 
        int soma = algarismoA + algarismoB;
        return soma;
    }

    public static int sub(int algarismoA, int algarismoB) { 
        int sub = algarismoA - algarismoB;
        return sub;
    }

    public static int mult(int algarismoA, int algarismoB) { 
        int mult = algarismoA * algarismoB;
        return mult;
    }

    public static int div(int algarismoA, int algarismoB) { 
        int div = algarismoA / algarismoB;
        return div;
    }

}
